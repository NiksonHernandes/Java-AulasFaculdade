
public class Engenheiro extends Profissional {
	private long crea;
	private String area;
	private String escritorio;
	
	public long getCrea() {
		return crea;
	}
	public void setCrea(long crea) {
		this.crea = crea;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getEscritorio() {
		return escritorio;
	}
	public void setEscritorio(String escritorio) {
		this.escritorio = escritorio;
	}
	
	
}
