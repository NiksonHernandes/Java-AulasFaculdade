public class Aparelho{
	private String nome;
	private Aparelho incorporado[];
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Aparelho[] getIncorporado() {
		return incorporado;
	}
	public void setIncorporado(Aparelho[] incorporado) {
		this.incorporado = incorporado;
	}
	
}
